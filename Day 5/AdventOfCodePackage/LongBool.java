package AdventOfCodePackage;
public class LongBool {
    public Long num;
    public boolean bool;

    public Long getNum(){
        return num;
    }
    public boolean getBool(){
        return bool;
    }
    public boolean setNum(Long x){
        num = x;
        return true;
    }
    public boolean setBool(boolean x){
        bool = x;
        return true;
    }
}
